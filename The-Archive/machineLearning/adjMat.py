def iamm(a,b,itr=1):
    for i in range(itr):
        a = np.matmul(a,b)
    return a

def imax(a,b,itr=1):
    if itr < 1:
        return a
    x = iamm(a,b)
    x[x<0] = 0
    for i in range(itr-1):
        x = iamm(x,b)
        x[x<0] = 0
    return x

def ELU(a,B):
    B[B<0] = a * ((e**B[B<0]) - 1)
    return B

def SELU(y,a,B):
    return y * ELU(a,B)

